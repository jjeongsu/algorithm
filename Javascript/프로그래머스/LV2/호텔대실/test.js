import solution from './solution.js';

describe('호텔대실 문제 테스트', () => {
  test.each([
    [
      [
        ['15:00', '17:00'],
        ['16:40', '18:20'],
        ['14:20', '15:20'],
        ['14:10', '19:20'],
        ['18:20', '21:20'],
      ],
      3,
    ],
    [
      [
        ['10:50', '11:10'],
        ['10:20', '10:40'],
        ['10:00', '10:10'],
      ],
      1,
    ],
    [
      [
        ['00:00', '00:07'],
        ['00:01', '00:08'],
        ['00:02', '00:09'],
        ['10:26', '10:41'],
      ],
      3,
    ],
  ])('올바른 객실수를 리턴하는가?', (input, expected) => {
    expect(solution(input)).toBe(expected);
  });
});
